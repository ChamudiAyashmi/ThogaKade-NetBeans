    private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed
       ArrayList<Item> itemList = null;
        try {
            itemList = ItemController.getAllItems();
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ViewCustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ViewCustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultTableModel dtm=(DefaultTableModel) tblItem.getModel();
        dtm.setRowCount(0);
        for (Item item : itemList) {
            Object[] rowData={item.getCode(),item.getDescription(),item.getUnitPrice(),item.getQtyOnHand()};
            dtm.addRow(rowData);
        }
        
    }//GEN-LAST:event_btnRefreshActionPerformed

